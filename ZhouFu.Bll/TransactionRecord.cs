		public List<ZhongLi.Model.TransactionRecord> DataTableToList(DataTable dt)
		{
			List<ZhongLi.Model.TransactionRecord> modelList = new List<ZhongLi.Model.TransactionRecord>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				ZhongLi.Model.TransactionRecord model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

